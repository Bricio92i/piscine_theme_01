  def get_workingtimes_by_userID!(user_id) do
    Repo.all(from(workingtime in Workingtime, where: workingtime.user_id == ^user_id))
  end

  ## get_workingtime by user_id and start - end
  def get_workingtime_by_userID_start_end!(user_id, wt_start, wt_end) do
    wt_st = Timex.parse!(wt_start, "%Y-%m-%d %H:%M:%S", :strftime)
    wt_nd = Timex.parse!(wt_end, "%Y-%m-%d %H:%M:%S", :strftime)
    Repo.all(from(worktime in Workingtime, where: worktime.user_id == ^user_id, where: worktime.start == ^wt_st, where: worktime.end == ^wt_nd))
  end

  ## get_workingtime by user_id and workingtime_id
  def get_workingtime_by_userID_wtID!(user_id, workingtime_id) do
    Repo.get_by(Workingtime, [user_id: user_id, id: workingtime_id])
  end

